-- Enable UUID extension
create extension if not exists "uuid-ossp";

-- Create tables
create table if not exists students (
  id uuid default uuid_generate_v4() primary key,
  full_name text not null,
  student_id text unique not null,
  belt_rank text not null,
  dojo text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists events (
  id uuid default uuid_generate_v4() primary key,
  name text not null,
  date date not null,
  location text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create table if not exists participations (
  id uuid default uuid_generate_v4() primary key,
  student_id uuid references students(id) on delete cascade not null,
  event_id uuid references events(id) on delete cascade not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(student_id, event_id)
);

create table if not exists results (
  id uuid default uuid_generate_v4() primary key,
  participation_id uuid references participations(id) on delete cascade not null,
  category text not null, -- e.g., 'Individual Kata', 'Team Kumite'
  medal text check (medal in ('GOLD', 'SILVER', 'BRONZE', 'PARTICIPATION')),
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

