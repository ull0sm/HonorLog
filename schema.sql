-- Participations (Randomly assign students to events)
insert into participations (student_id, event_id)
select s.id, e.id
from students s
cross join events e
where random() < 0.3; -- 30% chance of participation per event

-- Results (Randomly assign medals to participations)
insert into results (participation_id, category, medal)
select 
  p.id,
  case (random() * 3)::int
    when 0 then 'Individual Kata'
    when 1 then 'Individual Kumite'
    when 2 then 'Team Kata'
    else 'Team Kumite'
  end as category,
  case (random() * 4)::int
    when 0 then 'GOLD'
    when 1 then 'SILVER'
    when 2 then 'BRONZE'
    else 'PARTICIPATION'
  end as medal
from participations p
where random() < 0.5; -- 50% chance of getting a result if participated
